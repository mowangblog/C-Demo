/*Fibonacci数列的递推公式为：Fn=Fn-1+Fn-2，其中F1=F2=1。
当n比较大时，Fn也非常大，现在我们想知道，Fn除以10007的余数是多少。

输入格式

输入包含一个整数n。

输出格式

输出一行，包含一个整数，表示Fn除以10007的余数。 
说明：在本题中，答案是要求Fn除以10007的余数，因此我们只要能算出这个余数即可,
而不需要先计算出Fn的准确值，再将计算的结果除以10007取余数，直接计算余数往往比先算出原数再取余简单。*/

#include<stdio.h>    
#define M 10007    

int main()    
{    
    int a1,a2;    
    a1=a2=1;    
    int temp; //temp作为数据交换的中介 
    long n; //因为n>=1 and n<=1000000   
    long i;    
    scanf("%ld",&n);    

    for(i=1;i<n;i++)    
    {     
        //a1 作为f(n);a2为f(n+1) 
        //a1+a2=f(n+2)
        temp=a2;
        a2=(a1+a2)%M;
        a1=temp;
    }   
    printf("%d\n",a1);
    
    return 0;    
}         
    
